# First Stack Example
# Initialize the stack
stack = []

# Push elements onto the stack
stack.append('A')
stack.append('R')
stack.append('M')
stack.append('I')
stack.append('E')

print('Initial stack:')
print(stack)

# Pop elements from the stack
stack.pop()
stack.pop()

print('\nAfter Popped:')
print(stack)

# Push another element onto the stack
stack.append('Y')

print('\nPushed stack:')
print(stack)
